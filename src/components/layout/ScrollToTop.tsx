import { useEffect } from 'react';
import { useLocation } from 'react-router-dom';

export default function ScrollToTop() {
  const { pathname } = useLocation();

  useEffect(() => {
    window.scrollTo(0, 0);
  }, [pathname]);

  return null;
}

{/* Este componente sirve para mantener la posición exacta en la que estabas cuando navegues por los otros compontentes.- */}